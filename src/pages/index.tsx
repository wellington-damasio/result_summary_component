import Head from 'next/head'
import {Hanken_Grotesk} from 'next/font/google'
import ResultSummaryCard, {ISkillResult } from '../components/ResultSummaryCard'

const defaultFont = Hanken_Grotesk({
  weight: ['500', '700', '800'],
  subsets: ['latin']
})

interface IHomeProps{
  results: ISkillResult[]
  errorCode: boolean
}

export default function Home({results, errorCode}: IHomeProps) {
  if(errorCode) {
    return(
      <h1>Error...</h1>
    )
  }

  return (
    <>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/images/favicon-32x32.png" />
        <title>Results Summary</title>
      </Head>
      <main className={defaultFont.className}>
        
        <ResultSummaryCard results={results}/>
      </main>
    </>
  )
}

export async function getServerSideProps() {
  const productionUrl = 'https://result-summary-damasio.netlify.app/api/results'
  const devUrl = 'http://localhost:3000/api/results'
  
  const response = await fetch(productionUrl)
  const data = await response.json()
  const errorCode = response.ok ? false : response.status

  return {
    props: {
      results: data,
      errorCode
    }
  }
}