import Head from 'next/head'
import {Hanken_Grotesk} from 'next/font/google'
import ResultSummaryCard, {ISkillResult } from '../components/ResultSummaryCard'

const defaultFont = Hanken_Grotesk({
  weight: ['500', '700', '800'],
  subsets: ['latin']
})

interface IHomeProps{
  results: ISkillResult[]
}

export default function Home({results}: IHomeProps) {
  return (
    <>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/images/favicon-32x32.png" />
        <title>Results Summary</title>
      </Head>
      <main className={defaultFont.className}>
        
        <ResultSummaryCard results={results}/>
      </main>
    </>
  )
}

export async function getServerSideProps() {
  const response = await fetch('http://localhost:3000/api/results')
  const data = await response.json()

  return {
    props: {
      results: data
    }
  }
}